# while ციკლის გამოყენებით დაპრინტეთ 1-დან 50-მდე ყველა 4-ის ჯერადი რიცხვის კუბი

num=1              #შევქმენით ნუმ ცვლადი მნიშვნელობა მივანიჭეთ ერთი
while num<50:        #ვიწყებთ ციკლს, რომელიც გაგრძელდება, სანამ 'num' 50-ზე ნაკლებია
    num=num+1         #ანუ num-ის მნიშვნელობას ვზრდით ერთით
    if num%4==0:      # აქ ვამბობთ რომ თუ num-ის  4-ზე გაყოფისას ნაშთია 0
        num1=num**3     # num1 ტოლია num- ის კუბის 
        print(num1)     # ვპრინტავთ num1-ს 